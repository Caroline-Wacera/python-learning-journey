# 🌐 WHAT IS THE INTERNET?
# The internet is a network of computers that communicate with each other.

# 🖥️ CLIENT
# The device used by a user (phone, laptop, browser)

# 🗄️ SERVER
# A computer that stores, processes, and sends data when requested

# 📩 REQUEST
# When a client asks the server for data or a service

# 🔁 RESPONSE
# The data the server sends back to the client after a request

# 💡 SIMPLE FLOW
# Client → Request → Server → Response → Client


